<h3>12 Step Meeting List Xtras - Settings</h3>
<h4>Schema Settings</h4>
<p>Schema settings control the placement of schema.org structured data to your meetings pages. Structured data helps search engines better understand data on your page by presenting it in a structured way. This data isn't visible to users, but is contained in your page.</p>
<p><strong>Include Schema.org markup</strong>: Toggle on or off to include the structured data.</p>
<p><strong>Default Image</strong>: A link to this file will be included as a logo element on all meetings. Usually it's best to use your fellowship's logo or other mark that works for ALL meetings.</p>
<h4>Extra Content</h4>
<p>HTML content that is placeed above and below the accordion table if you use the [txmlx] shortcode or block on your site.</p>
<h4>Colors</h4>
<p>Color scheme for the accordion table if you use the [tsmlx] shortcode or block.</p>
<h4>Other Settings</h4>
<p><strong>Disable meetings page</strong>: This disables the main /meetings page, in case you are using one of our blocks or a shortcode on another page (like your homepage) and don't want the meetings page indexed by search engines.</p>
<p><strong>Enable unique group ID field</strong>: Toggle this setting to add or remove the Group Number field on your meeting edit pages. The contents of this field is not shown to the user, but is a handy place to store a GSO number or other unique identifier for a group.</p>
<p><em>NOTE: This field is saved along with other Group data, not specific meeting data ,so changing it will change it for all meetings under that group.</em></p>